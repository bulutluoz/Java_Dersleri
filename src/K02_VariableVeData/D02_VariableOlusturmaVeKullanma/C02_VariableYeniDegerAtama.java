package K02_VariableVeData.D02_VariableOlusturmaVeKullanma;

public class C02_VariableYeniDegerAtama {

    public static void main(String[] args) {


        int sayi = 10;

        System.out.println("10.satirda sayi : " + sayi);


        sayi = 15;

        System.out.println("15.satirda sayi : " + sayi);


        sayi = sayi + 10;

        System.out.println("20.satirda sayi : " + sayi);





    }



}
